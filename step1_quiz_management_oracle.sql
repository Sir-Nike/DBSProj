CREATE OR REPLACE PACKAGE BODY PKG_QUIZ_ADMIN AS
    G_ADMIN_CLEANUP_MODE BOOLEAN := FALSE;

    PROCEDURE SET_ADMIN_CLEANUP_MODE(
        P_ENABLED IN NUMBER
    ) IS
    BEGIN
        G_ADMIN_CLEANUP_MODE := (P_ENABLED = 1);
    END;

    FUNCTION IS_ADMIN_CLEANUP_MODE
        RETURN NUMBER
    IS
    BEGIN
        RETURN CASE WHEN G_ADMIN_CLEANUP_MODE THEN 1 ELSE 0 END;
    END;

    PROCEDURE VALIDATE_QUIZ_FOR_PUBLISH(
        P_QUIZ_ID IN QUIZ.QUIZ_ID%TYPE
    ) IS
        CURSOR C_Q IS
            SELECT QUESTION_ID
              FROM QUESTION
             WHERE QUIZ_ID = P_QUIZ_ID;

        V_QUESTION_COUNT NUMBER := 0;
        V_ATTEMPT_COUNT NUMBER := 0;
        V_OPTION_COUNT  NUMBER;
        V_CORRECT_COUNT NUMBER;
    BEGIN
        SELECT COUNT(*)
          INTO V_QUESTION_COUNT
          FROM QUESTION
         WHERE QUIZ_ID = P_QUIZ_ID;

        IF V_QUESTION_COUNT = 0 THEN
            RAISE_APPLICATION_ERROR(-20200, 'A quiz must contain at least one question before publishing.');
        END IF;

        SELECT COUNT(*)
          INTO V_ATTEMPT_COUNT
          FROM QUIZ_ATTEMPT
         WHERE QUIZ_ID = P_QUIZ_ID;

        IF V_ATTEMPT_COUNT = 0 THEN
            RAISE_APPLICATION_ERROR(-20204, 'A quiz must have at least one attempt before publishing results.');
        END IF;

        FOR R IN C_Q LOOP
            SELECT COUNT(*),
                   SUM(CASE WHEN IS_CORRECT = 'Y' THEN 1 ELSE 0 END)
              INTO V_OPTION_COUNT, V_CORRECT_COUNT
              FROM QUESTION_OPTION
             WHERE QUESTION_ID = R.QUESTION_ID;

            IF V_OPTION_COUNT <> 4 THEN
                RAISE_APPLICATION_ERROR(-20201, 'Each question must have exactly four options before publishing.');
            END IF;

            IF NVL(V_CORRECT_COUNT, 0) <> 1 THEN
                RAISE_APPLICATION_ERROR(-20202, 'Each question must have exactly one correct option.');
            END IF;
        END LOOP;
    END;

    PROCEDURE CREATE_SUBJECT(
        P_SUBJECT_NAME   IN SUBJECT.SUBJECT_NAME%TYPE,
        P_SUBJECT_CODE   IN SUBJECT.SUBJECT_CODE%TYPE,
        P_SEMESTER       IN SUBJECT.SEMESTER%TYPE,
        P_DEPARTMENT_ID  IN SUBJECT.DEPARTMENT_ID%TYPE,
        P_SUBJECT_ID     OUT SUBJECT.SUBJECT_ID%TYPE
    ) IS
    BEGIN
        INSERT INTO SUBJECT (SUBJECT_NAME, SUBJECT_CODE, SEMESTER, DEPARTMENT_ID)
        VALUES (P_SUBJECT_NAME, UPPER(TRIM(P_SUBJECT_CODE)), P_SEMESTER, P_DEPARTMENT_ID)
        RETURNING SUBJECT_ID INTO P_SUBJECT_ID;
    END;

    PROCEDURE CREATE_QUIZ(
        P_QUIZ_TITLE         IN QUIZ.QUIZ_TITLE%TYPE,
        P_DURATION_MINUTES   IN QUIZ.DURATION_MINUTES%TYPE,
        P_QUIZ_DATE          IN QUIZ.QUIZ_DATE%TYPE,
        P_SUBJECT_ID         IN QUIZ.SUBJECT_ID%TYPE,
        P_CREATED_BY         IN QUIZ.CREATED_BY%TYPE,
        P_QUIZ_ID            OUT QUIZ.QUIZ_ID%TYPE
    ) IS
    BEGIN
        INSERT INTO QUIZ (QUIZ_TITLE, DURATION_MINUTES, QUIZ_DATE, SUBJECT_ID, CREATED_BY)
        VALUES (P_QUIZ_TITLE, P_DURATION_MINUTES, P_QUIZ_DATE, P_SUBJECT_ID, P_CREATED_BY)
        RETURNING QUIZ_ID INTO P_QUIZ_ID;
    END;

    PROCEDURE ADD_QUESTION(
        P_QUIZ_ID         IN QUESTION.QUIZ_ID%TYPE,
        P_QUESTION_TEXT   IN QUESTION.QUESTION_TEXT%TYPE,
        P_MARKS           IN QUESTION.MARKS%TYPE,
        P_DISPLAY_ORDER   IN QUESTION.DISPLAY_ORDER%TYPE,
        P_QUESTION_ID     OUT QUESTION.QUESTION_ID%TYPE
    ) IS
        V_NEXT_ORDER NUMBER;
        V_DUMMY_QUIZ_ID QUIZ.QUIZ_ID%TYPE;
    BEGIN
        IF P_QUIZ_ID IS NULL THEN
            RAISE_APPLICATION_ERROR(-20210, 'Quiz id is required when adding a question.');
        END IF;

        IF TRIM(P_QUESTION_TEXT) IS NULL THEN
            RAISE_APPLICATION_ERROR(-20211, 'Question text cannot be blank.');
        END IF;

        IF P_MARKS IS NULL OR P_MARKS <= 0 THEN
            RAISE_APPLICATION_ERROR(-20212, 'Question marks must be greater than zero.');
        END IF;

        SELECT QUIZ_ID
          INTO V_DUMMY_QUIZ_ID
          FROM QUIZ
         WHERE QUIZ_ID = P_QUIZ_ID
         FOR UPDATE;

        IF P_DISPLAY_ORDER IS NULL OR P_DISPLAY_ORDER <= 0 THEN
            SELECT NVL(MAX(DISPLAY_ORDER), 0) + 1
              INTO V_NEXT_ORDER
              FROM QUESTION
             WHERE QUIZ_ID = P_QUIZ_ID;
        ELSE
            V_NEXT_ORDER := P_DISPLAY_ORDER;
        END IF;

        INSERT INTO QUESTION (QUIZ_ID, QUESTION_TEXT, MARKS, DISPLAY_ORDER)
        VALUES (P_QUIZ_ID, TRIM(P_QUESTION_TEXT), P_MARKS, V_NEXT_ORDER)
        RETURNING QUESTION_ID INTO P_QUESTION_ID;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20213, 'Quiz not found for the provided quiz id.');
    END;

    PROCEDURE ADD_OPTION(
        P_QUESTION_ID     IN QUESTION_OPTION.QUESTION_ID%TYPE,
        P_OPTION_TEXT     IN QUESTION_OPTION.OPTION_TEXT%TYPE,
        P_IS_CORRECT      IN QUESTION_OPTION.IS_CORRECT%TYPE,
        P_DISPLAY_ORDER   IN QUESTION_OPTION.DISPLAY_ORDER%TYPE,
        P_OPTION_ID       OUT QUESTION_OPTION.OPTION_ID%TYPE
    ) IS
        V_DUMMY_QUESTION_ID QUESTION.QUESTION_ID%TYPE;
        V_NEXT_ORDER      NUMBER;
        V_FLAG            VARCHAR2(1);
    BEGIN
        IF P_QUESTION_ID IS NULL THEN
            RAISE_APPLICATION_ERROR(-20214, 'Question id is required when adding an option.');
        END IF;

        IF TRIM(P_OPTION_TEXT) IS NULL THEN
            RAISE_APPLICATION_ERROR(-20215, 'Option text cannot be blank.');
        END IF;

        V_FLAG := UPPER(TRIM(P_IS_CORRECT));
        IF V_FLAG NOT IN ('Y', 'N') THEN
            RAISE_APPLICATION_ERROR(-20216, 'Option correctness must be Y or N.');
        END IF;

        SELECT QUESTION_ID
          INTO V_DUMMY_QUESTION_ID
          FROM QUESTION
         WHERE QUESTION_ID = P_QUESTION_ID
         FOR UPDATE;

        IF P_DISPLAY_ORDER IS NULL OR P_DISPLAY_ORDER <= 0 THEN
            SELECT NVL(MAX(DISPLAY_ORDER), 0) + 1
              INTO V_NEXT_ORDER
              FROM QUESTION_OPTION
             WHERE QUESTION_ID = P_QUESTION_ID;
        ELSE
            V_NEXT_ORDER := P_DISPLAY_ORDER;
        END IF;

        INSERT INTO QUESTION_OPTION (QUESTION_ID, OPTION_TEXT, IS_CORRECT, DISPLAY_ORDER)
        VALUES (P_QUESTION_ID, TRIM(P_OPTION_TEXT), V_FLAG, V_NEXT_ORDER)
        RETURNING OPTION_ID INTO P_OPTION_ID;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20217, 'Question not found for the provided question id.');
    END;

    PROCEDURE RECOMPUTE_QUIZ_TOTALS(
        P_QUIZ_ID IN QUIZ.QUIZ_ID%TYPE
    ) IS
    BEGIN
        UPDATE QUIZ
           SET TOTAL_MARKS = NVL((SELECT SUM(MARKS) FROM QUESTION WHERE QUIZ_ID = P_QUIZ_ID), 0)
         WHERE QUIZ_ID = P_QUIZ_ID;
    END;

    PROCEDURE PUBLISH_RESULTS(
        P_QUIZ_ID     IN QUIZ.QUIZ_ID%TYPE,
        P_TEACHER_ID  IN TEACHER.TEACHER_ID%TYPE
    ) IS
        CURSOR C_ATTEMPTS IS
            SELECT ATTEMPT_ID
              FROM QUIZ_ATTEMPT
             WHERE QUIZ_ID = P_QUIZ_ID
               AND STATUS IN ('SUBMITTED', 'AUTO_SUBMITTED')
             FOR UPDATE;

        V_OWNER_ID QUIZ.CREATED_BY%TYPE;
        V_SCORE    NUMBER;
    BEGIN
        SELECT CREATED_BY
          INTO V_OWNER_ID
          FROM QUIZ
         WHERE QUIZ_ID = P_QUIZ_ID
         FOR UPDATE;

        IF V_OWNER_ID <> P_TEACHER_ID THEN
            RAISE_APPLICATION_ERROR(-20203, 'Only the quiz creator can publish results.');
        END IF;

        VALIDATE_QUIZ_FOR_PUBLISH(P_QUIZ_ID);

        FOR R IN C_ATTEMPTS LOOP
            V_SCORE := PKG_QUIZ_ATTEMPT.COMPUTE_ATTEMPT_SCORE(R.ATTEMPT_ID);
            UPDATE QUIZ_ATTEMPT
               SET TOTAL_SCORE = V_SCORE
             WHERE CURRENT OF C_ATTEMPTS;
        END LOOP;

        UPDATE QUIZ
           SET RESULTS_PUBLISHED = 'Y'
         WHERE QUIZ_ID = P_QUIZ_ID;

        INSERT INTO RESULT_PUBLISH_LOG (QUIZ_ID, PUBLISHED_BY)
        VALUES (P_QUIZ_ID, P_TEACHER_ID);
    END;

    PROCEDURE UNPUBLISH_RESULTS(
        P_QUIZ_ID     IN QUIZ.QUIZ_ID%TYPE,
        P_TEACHER_ID  IN TEACHER.TEACHER_ID%TYPE
    ) IS
        V_OWNER_ID QUIZ.CREATED_BY%TYPE;
    BEGIN
        SELECT CREATED_BY
          INTO V_OWNER_ID
          FROM QUIZ
         WHERE QUIZ_ID = P_QUIZ_ID
         FOR UPDATE;

        IF V_OWNER_ID <> P_TEACHER_ID THEN
            RAISE_APPLICATION_ERROR(-20204, 'Only the quiz creator can unpublish results.');
        END IF;

        UPDATE QUIZ
           SET RESULTS_PUBLISHED = 'N'
         WHERE QUIZ_ID = P_QUIZ_ID;
    END;
END PKG_QUIZ_ADMIN;
