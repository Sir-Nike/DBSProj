CREATE OR REPLACE PACKAGE BODY PKG_QUIZ_ATTEMPT AS
    PROCEDURE LOCK_ATTEMPT(
        P_ATTEMPT_ID IN QUIZ_ATTEMPT.ATTEMPT_ID%TYPE,
        P_QUIZ_ID    IN QUIZ.QUIZ_ID%TYPE DEFAULT NULL
    ) IS
        V_STATUS    QUIZ_ATTEMPT.STATUS%TYPE;
        V_QUIZ_ID   QUIZ_ATTEMPT.QUIZ_ID%TYPE;
        V_START     QUIZ_ATTEMPT.START_TIME%TYPE;
        V_DURATION  QUIZ.DURATION_MINUTES%TYPE;
    BEGIN
        SELECT QA.STATUS, QA.QUIZ_ID, QA.START_TIME, Q.DURATION_MINUTES
          INTO V_STATUS, V_QUIZ_ID, V_START, V_DURATION
          FROM QUIZ_ATTEMPT QA
          JOIN QUIZ Q ON Q.QUIZ_ID = QA.QUIZ_ID
         WHERE QA.ATTEMPT_ID = P_ATTEMPT_ID
         FOR UPDATE NOWAIT;

        IF P_QUIZ_ID IS NOT NULL AND V_QUIZ_ID <> P_QUIZ_ID THEN
            RAISE_APPLICATION_ERROR(-20101, 'Attempt does not belong to the expected quiz.');
        END IF;

        IF V_STATUS <> 'IN_PROGRESS' THEN
            RAISE_APPLICATION_ERROR(-20102, 'Attempt is no longer editable.');
        END IF;

        IF SYSDATE > (V_START + (V_DURATION / 1440)) THEN
            UPDATE QUIZ_ATTEMPT
               SET STATUS = 'AUTO_SUBMITTED',
                   SUBMISSION_TIME = SYSDATE,
                   TOTAL_SCORE = COMPUTE_ATTEMPT_SCORE(P_ATTEMPT_ID)
             WHERE ATTEMPT_ID = P_ATTEMPT_ID;

            RAISE_APPLICATION_ERROR(-20103, 'Quiz duration has expired. Attempt auto-submitted.');
        END IF;
    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE = -54 THEN
                RAISE_APPLICATION_ERROR(-20104, 'Attempt is locked by another transaction.');
            ELSE
                RAISE;
            END IF;
    END;

    FUNCTION START_ATTEMPT(
        P_QUIZ_ID     IN QUIZ_ATTEMPT.QUIZ_ID%TYPE,
        P_STUDENT_ID  IN QUIZ_ATTEMPT.STUDENT_ID%TYPE
    ) RETURN QUIZ_ATTEMPT.ATTEMPT_ID%TYPE
    IS
        V_ATTEMPT_ID  QUIZ_ATTEMPT.ATTEMPT_ID%TYPE;
        V_STATUS      QUIZ_ATTEMPT.STATUS%TYPE;
    BEGIN
        BEGIN
            SELECT ATTEMPT_ID, STATUS
              INTO V_ATTEMPT_ID, V_STATUS
              FROM QUIZ_ATTEMPT
             WHERE QUIZ_ID = P_QUIZ_ID
               AND STUDENT_ID = P_STUDENT_ID
             FOR UPDATE NOWAIT;

            IF V_STATUS IN ('SUBMITTED', 'AUTO_SUBMITTED') THEN
                RAISE_APPLICATION_ERROR(-20105, 'Student already has a completed attempt for this quiz.');
            END IF;

            RETURN V_ATTEMPT_ID;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                INSERT INTO QUIZ_ATTEMPT (QUIZ_ID, STUDENT_ID, START_TIME, LAST_SAVED_AT, STATUS)
                VALUES (P_QUIZ_ID, P_STUDENT_ID, SYSDATE, SYSDATE, 'IN_PROGRESS')
                RETURNING ATTEMPT_ID INTO V_ATTEMPT_ID;

                RETURN V_ATTEMPT_ID;
            WHEN OTHERS THEN
                IF SQLCODE = -54 THEN
                    RAISE_APPLICATION_ERROR(-20106, 'Quiz attempt row is locked by another transaction.');
                ELSE
                    RAISE;
                END IF;
        END;
    END;

    PROCEDURE AUTOSAVE_ANSWER(
        P_ATTEMPT_ID         IN QUIZ_ATTEMPT.ATTEMPT_ID%TYPE,
        P_QUESTION_ID        IN QUESTION.QUESTION_ID%TYPE,
        P_SELECTED_OPTION_ID IN QUESTION_OPTION.OPTION_ID%TYPE
    ) IS
        V_QUIZ_ID    QUESTION.QUIZ_ID%TYPE;
        V_ANSWER_ID  STUDENT_ANSWER.ANSWER_ID%TYPE;
    BEGIN
        SELECT QUIZ_ID
          INTO V_QUIZ_ID
          FROM QUESTION
         WHERE QUESTION_ID = P_QUESTION_ID;

        LOCK_ATTEMPT(P_ATTEMPT_ID, V_QUIZ_ID);

        BEGIN
            SELECT ANSWER_ID
              INTO V_ANSWER_ID
              FROM STUDENT_ANSWER
             WHERE ATTEMPT_ID = P_ATTEMPT_ID
               AND QUESTION_ID = P_QUESTION_ID
             FOR UPDATE NOWAIT;

            UPDATE STUDENT_ANSWER
               SET SELECTED_OPTION_ID = P_SELECTED_OPTION_ID,
                   LAST_UPDATED_AT = SYSDATE
             WHERE ANSWER_ID = V_ANSWER_ID;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                INSERT INTO STUDENT_ANSWER (ATTEMPT_ID, QUESTION_ID, SELECTED_OPTION_ID)
                VALUES (P_ATTEMPT_ID, P_QUESTION_ID, P_SELECTED_OPTION_ID);
            WHEN OTHERS THEN
                IF SQLCODE = -54 THEN
                    RAISE_APPLICATION_ERROR(-20107, 'Answer row is locked by another transaction.');
                ELSE
                    RAISE;
                END IF;
        END;
    END;

    PROCEDURE CLEAR_ANSWER(
        P_ATTEMPT_ID   IN QUIZ_ATTEMPT.ATTEMPT_ID%TYPE,
        P_QUESTION_ID  IN QUESTION.QUESTION_ID%TYPE
    ) IS
        V_QUIZ_ID QUESTION.QUIZ_ID%TYPE;
    BEGIN
        SELECT QUIZ_ID
          INTO V_QUIZ_ID
          FROM QUESTION
         WHERE QUESTION_ID = P_QUESTION_ID;

        LOCK_ATTEMPT(P_ATTEMPT_ID, V_QUIZ_ID);

        DELETE FROM STUDENT_ANSWER
         WHERE ATTEMPT_ID = P_ATTEMPT_ID
           AND QUESTION_ID = P_QUESTION_ID;
    END;

    FUNCTION COMPUTE_ATTEMPT_SCORE(
        P_ATTEMPT_ID IN QUIZ_ATTEMPT.ATTEMPT_ID%TYPE
    ) RETURN NUMBER
    IS
        CURSOR C_ANSWERS IS
            SELECT SA.ANSWER_ID, SA.SELECTED_OPTION_ID, Q.MARKS
              FROM STUDENT_ANSWER SA
              JOIN QUESTION Q ON Q.QUESTION_ID = SA.QUESTION_ID
             WHERE SA.ATTEMPT_ID = P_ATTEMPT_ID;

        V_TOTAL NUMBER := 0;
        V_GAIN  NUMBER := 0;
    BEGIN
        FOR R IN C_ANSWERS LOOP
            V_GAIN := CASE
                WHEN FN_IS_CORRECT_OPTION(R.SELECTED_OPTION_ID) = 1 THEN R.MARKS
                ELSE 0
            END;

            UPDATE STUDENT_ANSWER
               SET IS_CORRECT = CASE WHEN V_GAIN > 0 THEN 'Y' ELSE 'N' END,
                   LAST_UPDATED_AT = SYSDATE
             WHERE ANSWER_ID = R.ANSWER_ID;

            V_TOTAL := V_TOTAL + V_GAIN;
        END LOOP;

        RETURN NVL(V_TOTAL, 0);
    END;

    PROCEDURE SUBMIT_ATTEMPT(
        P_ATTEMPT_ID IN QUIZ_ATTEMPT.ATTEMPT_ID%TYPE
    ) IS
        V_SCORE NUMBER;
    BEGIN
        LOCK_ATTEMPT(P_ATTEMPT_ID);
        V_SCORE := COMPUTE_ATTEMPT_SCORE(P_ATTEMPT_ID);

        UPDATE QUIZ_ATTEMPT
           SET STATUS = 'SUBMITTED',
               SUBMISSION_TIME = SYSDATE,
               TOTAL_SCORE = V_SCORE,
               LAST_SAVED_AT = SYSDATE
         WHERE ATTEMPT_ID = P_ATTEMPT_ID;
    END;
END PKG_QUIZ_ATTEMPT;
