SELECT
    T.TEACHER_ID,
    T.NAME AS TEACHER_NAME,
    D.DEPARTMENT_NAME,
    S.SUBJECT_ID,
    S.SUBJECT_CODE,
    S.SUBJECT_NAME,
    Q.QUIZ_ID,
    Q.QUIZ_TITLE,
    Q.QUIZ_DATE,
    Q.DURATION_MINUTES,
    Q.TOTAL_MARKS,
    Q.RESULTS_PUBLISHED,
