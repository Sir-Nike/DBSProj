PROMPT Creating utility function...
CREATE OR REPLACE FUNCTION FN_IS_CORRECT_OPTION (
    P_OPTION_ID IN QUESTION_OPTION.OPTION_ID%TYPE
) RETURN NUMBER
IS
    V_IS_CORRECT QUESTION_OPTION.IS_CORRECT%TYPE;
BEGIN
    SELECT IS_CORRECT
      INTO V_IS_CORRECT
      FROM QUESTION_OPTION
     WHERE OPTION_ID = P_OPTION_ID;

    RETURN CASE WHEN V_IS_CORRECT = 'Y' THEN 1 ELSE 0 END;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RETURN 0;
END;
/

CREATE OR REPLACE FUNCTION FN_TEACHER_CODE_PREFIX (
    P_DEPARTMENT_ID IN DEPARTMENT.DEPARTMENT_ID%TYPE
) RETURN VARCHAR2
IS
    V_CODE DEPARTMENT.DEPARTMENT_CODE%TYPE;
BEGIN
    SELECT DEPARTMENT_CODE
      INTO V_CODE
      FROM DEPARTMENT
     WHERE DEPARTMENT_ID = P_DEPARTMENT_ID;

    RETURN UPPER(TRIM(V_CODE));
END;
/

CREATE OR REPLACE FUNCTION FN_NEXT_TEACHER_CODE (
    P_DEPARTMENT_ID IN DEPARTMENT.DEPARTMENT_ID%TYPE
) RETURN VARCHAR2
IS
    V_PREFIX  VARCHAR2(10);
    V_NUMBER  NUMBER;
BEGIN
    V_PREFIX := FN_TEACHER_CODE_PREFIX(P_DEPARTMENT_ID);

    SELECT NVL(MAX(TO_NUMBER(REGEXP_SUBSTR(TEACHER_CODE, '\d+$'))), 0) + 1
      INTO V_NUMBER
      FROM TEACHER
     WHERE TEACHER_CODE LIKE V_PREFIX || '%';

    RETURN V_PREFIX || LPAD(V_NUMBER, 3, '0');
END;
/

