PROMPT Creating views...
CREATE OR REPLACE VIEW VW_TEACHER_QUIZ_DASHBOARD AS
SELECT
    T.TEACHER_ID,
    T.NAME AS TEACHER_NAME,
    D.DEPARTMENT_NAME,
    S.SUBJECT_ID,
    S.SUBJECT_CODE,
    S.SUBJECT_NAME,
    Q.QUIZ_ID,
    Q.QUIZ_TITLE,
    Q.QUIZ_DATE,
    Q.DURATION_MINUTES,
    Q.TOTAL_MARKS,
    Q.RESULTS_PUBLISHED,
    COUNT(DISTINCT QQ.QUESTION_ID) AS QUESTION_COUNT,
    COUNT(DISTINCT QA.ATTEMPT_ID) AS ATTEMPT_COUNT,
    SUM(CASE WHEN QA.STATUS IN ('SUBMITTED', 'AUTO_SUBMITTED') THEN 1 ELSE 0 END) AS SUBMITTED_COUNT,
    ROUND(AVG(CASE WHEN QA.STATUS IN ('SUBMITTED', 'AUTO_SUBMITTED') THEN QA.TOTAL_SCORE END), 2) AS AVG_SCORE,
    MAX(CASE WHEN QA.STATUS IN ('SUBMITTED', 'AUTO_SUBMITTED') THEN QA.TOTAL_SCORE END) AS TOP_SCORE
FROM TEACHER T
JOIN DEPARTMENT D ON D.DEPARTMENT_ID = T.DEPARTMENT_ID
JOIN QUIZ Q ON Q.CREATED_BY = T.TEACHER_ID
JOIN SUBJECT S ON S.SUBJECT_ID = Q.SUBJECT_ID
LEFT JOIN QUESTION QQ ON QQ.QUIZ_ID = Q.QUIZ_ID
LEFT JOIN QUIZ_ATTEMPT QA ON QA.QUIZ_ID = Q.QUIZ_ID
GROUP BY
    T.TEACHER_ID, T.NAME, D.DEPARTMENT_NAME,
    S.SUBJECT_ID, S.SUBJECT_CODE, S.SUBJECT_NAME,
