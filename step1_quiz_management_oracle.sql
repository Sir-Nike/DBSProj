    PROCEDURE PUBLISH_RESULTS(
        P_QUIZ_ID     IN QUIZ.QUIZ_ID%TYPE,
        P_TEACHER_ID  IN TEACHER.TEACHER_ID%TYPE
    ) IS
        CURSOR C_ATTEMPTS IS
            SELECT ATTEMPT_ID
              FROM QUIZ_ATTEMPT
             WHERE QUIZ_ID = P_QUIZ_ID
               AND STATUS IN ('SUBMITTED', 'AUTO_SUBMITTED')
             FOR UPDATE;

        V_OWNER_ID QUIZ.CREATED_BY%TYPE;
        V_SCORE    NUMBER;
    BEGIN
        SELECT CREATED_BY
          INTO V_OWNER_ID
          FROM QUIZ
         WHERE QUIZ_ID = P_QUIZ_ID
         FOR UPDATE;

        IF V_OWNER_ID <> P_TEACHER_ID THEN
            RAISE_APPLICATION_ERROR(-20203, 'Only the quiz creator can publish results.');
        END IF;

        VALIDATE_QUIZ_FOR_PUBLISH(P_QUIZ_ID);

        FOR R IN C_ATTEMPTS LOOP
            V_SCORE := PKG_QUIZ_ATTEMPT.COMPUTE_ATTEMPT_SCORE(R.ATTEMPT_ID);
            UPDATE QUIZ_ATTEMPT
               SET TOTAL_SCORE = V_SCORE
             WHERE CURRENT OF C_ATTEMPTS;
        END LOOP;

        UPDATE QUIZ
           SET RESULTS_PUBLISHED = 'Y'
         WHERE QUIZ_ID = P_QUIZ_ID;

        INSERT INTO RESULT_PUBLISH_LOG (QUIZ_ID, PUBLISHED_BY)
        VALUES (P_QUIZ_ID, P_TEACHER_ID);
    END;

