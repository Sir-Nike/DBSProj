CREATE OR REPLACE TRIGGER TRG_BIU_STUDENT_ANSWER_VALIDATE
BEFORE INSERT OR UPDATE ON STUDENT_ANSWER
FOR EACH ROW
DECLARE
    V_ATTEMPT_QUIZ_ID   QUIZ_ATTEMPT.QUIZ_ID%TYPE;
    V_QUESTION_QUIZ_ID  QUESTION.QUIZ_ID%TYPE;
    V_OPTION_QUESTION   QUESTION_OPTION.QUESTION_ID%TYPE;
BEGIN
    SELECT QUIZ_ID
      INTO V_ATTEMPT_QUIZ_ID
      FROM QUIZ_ATTEMPT
     WHERE ATTEMPT_ID = :NEW.ATTEMPT_ID;

    SELECT QUIZ_ID
      INTO V_QUESTION_QUIZ_ID
      FROM QUESTION
     WHERE QUESTION_ID = :NEW.QUESTION_ID;

    IF V_ATTEMPT_QUIZ_ID <> V_QUESTION_QUIZ_ID THEN
        RAISE_APPLICATION_ERROR(-20004, 'Answered question does not belong to the attempted quiz.');
    END IF;

    IF :NEW.SELECTED_OPTION_ID IS NOT NULL THEN
        SELECT QUESTION_ID
          INTO V_OPTION_QUESTION
          FROM QUESTION_OPTION
         WHERE OPTION_ID = :NEW.SELECTED_OPTION_ID;

        IF V_OPTION_QUESTION <> :NEW.QUESTION_ID THEN
            RAISE_APPLICATION_ERROR(-20005, 'Selected option does not belong to the answered question.');
        END IF;
    END IF;

    :NEW.LAST_UPDATED_AT := SYSDATE;
END;
/

PROMPT Creating autosave and derived-total triggers...
CREATE OR REPLACE TRIGGER TRG_AIUD_STUDENT_ANSWER_AUTOSAVE
AFTER INSERT OR UPDATE OR DELETE ON STUDENT_ANSWER
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        UPDATE QUIZ_ATTEMPT
           SET LAST_SAVED_AT = SYSDATE
         WHERE ATTEMPT_ID = :NEW.ATTEMPT_ID;

        INSERT INTO ANSWER_AUTOSAVE_LOG (ANSWER_ID, ATTEMPT_ID, QUESTION_ID, OPERATION_TYPE)
        VALUES (:NEW.ANSWER_ID, :NEW.ATTEMPT_ID, :NEW.QUESTION_ID, 'INSERT');
    ELSIF UPDATING THEN
        UPDATE QUIZ_ATTEMPT
           SET LAST_SAVED_AT = SYSDATE
         WHERE ATTEMPT_ID = :NEW.ATTEMPT_ID;

        INSERT INTO ANSWER_AUTOSAVE_LOG (ANSWER_ID, ATTEMPT_ID, QUESTION_ID, OPERATION_TYPE)
        VALUES (:NEW.ANSWER_ID, :NEW.ATTEMPT_ID, :NEW.QUESTION_ID, 'UPDATE');
    ELSE
        UPDATE QUIZ_ATTEMPT
           SET LAST_SAVED_AT = SYSDATE
         WHERE ATTEMPT_ID = :OLD.ATTEMPT_ID;

        INSERT INTO ANSWER_AUTOSAVE_LOG (ANSWER_ID, ATTEMPT_ID, QUESTION_ID, OPERATION_TYPE)
        VALUES (:OLD.ANSWER_ID, :OLD.ATTEMPT_ID, :OLD.QUESTION_ID, 'DELETE');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_QUESTION_TOTALS_CT
FOR INSERT OR UPDATE OR DELETE ON QUESTION
COMPOUND TRIGGER
    TYPE T_ID_TAB IS TABLE OF NUMBER INDEX BY PLS_INTEGER;
    G_QUIZ_IDS T_ID_TAB;
    G_COUNT PLS_INTEGER := 0;

    PROCEDURE ADD_ID(P_QUIZ_ID NUMBER) IS
        V_FOUND BOOLEAN := FALSE;
    BEGIN
        IF P_QUIZ_ID IS NULL THEN
            RETURN;
        END IF;

        FOR I IN 1 .. G_COUNT LOOP
            IF G_QUIZ_IDS(I) = P_QUIZ_ID THEN
                V_FOUND := TRUE;
                EXIT;
            END IF;
        END LOOP;

        IF NOT V_FOUND THEN
            G_COUNT := G_COUNT + 1;
            G_QUIZ_IDS(G_COUNT) := P_QUIZ_ID;
        END IF;
    END;

    AFTER EACH ROW IS
    BEGIN
        ADD_ID(NVL(:NEW.QUIZ_ID, :OLD.QUIZ_ID));
        IF UPDATING AND :OLD.QUIZ_ID <> :NEW.QUIZ_ID THEN
            ADD_ID(:OLD.QUIZ_ID);
            ADD_ID(:NEW.QUIZ_ID);
        END IF;
    END AFTER EACH ROW;

    AFTER STATEMENT IS
    BEGIN
        FOR I IN 1 .. G_COUNT LOOP
            UPDATE QUIZ
               SET TOTAL_MARKS = NVL((SELECT SUM(MARKS) FROM QUESTION WHERE QUIZ_ID = G_QUIZ_IDS(I)), 0)
             WHERE QUIZ_ID = G_QUIZ_IDS(I);
        END LOOP;
    END AFTER STATEMENT;
END;
